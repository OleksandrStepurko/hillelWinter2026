package com.hillel.cw_03.ex;

public class MaxFromtwo {

    private static int max(int a, int b) {
        return a > b ? a : b;
    }

    private static int maxInt(int a, int b) {
        return Integer.max(a, b);
    }


    private static int maxMath(int a, int b) {
        return Math.max(a, b);
    }

}
